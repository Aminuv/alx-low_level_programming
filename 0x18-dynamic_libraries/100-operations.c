#include <stdio.h>
/**
 * add - add a num
 * @a: num
 * @b: num
 * Return: result
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - sub a num
 * @a: num
 * @b: num
 * Return: result
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplai a num
 * @a: num
 * @b: num
 * Return: result
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divis a num
 * @a: num
 * @b: num
 * Return: result
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modul a num
 * @a: num
 * @b: num
 * Return: result
 */

int mod(int a, int b)
{
	return (a % b);
}
